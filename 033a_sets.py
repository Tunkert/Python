# a set is unordered, unchangeable, and un-indexed - but you can remove and add items
my_names_set = {"Tim", "Sarah", "Joe", "John", "Amy"}

print(my_names_set)  # this will not always print in the same order

# sets cannot have duplicates so
my_other_set = {"Tim", "Tim", "Sarah", "Joe", "Matt", "Amy"}

print(my_other_set)  # notice Tim is printed only once
