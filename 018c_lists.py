# list slicing
names_list = ["Tim", "Sarah", "Sean", "Joe", "Aaron", "Matt", "Barry", "Chet"]

# like strings [start:stop:step]

# reverse list
print(names_list[::-1])

# first three items in the list
print(names_list[:3:])

# last three items in the list
print(names_list[-3::])

# every other item in the list
print(names_list[::2])

# every other item in the list in reverse
print(names_list[::-2])

