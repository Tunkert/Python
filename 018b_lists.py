my_names_list = ["Tim", "Sarah", "Joe", "Sean", "Chet", "Barry", "Aaron", "Matt"]

# print the first item on the list
print(my_names_list[0])

# print the last item on the list
print(my_names_list[-1])

# find if an item is in a list
print("Sarah" in my_names_list)  # returns the boolean value True

# find if an item is not in a list
print("Tim" not in my_names_list)  # returns the boolean value False

# another example
print("Joey" not in my_names_list)  # returns the boolean value True
