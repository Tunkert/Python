my_dict = {
    "name": "Timothy Unkert",
    "age": 46,
    "GPA": 4.0
}

# iterate through the keys of a dictionary
for k in my_dict.keys():
    print(k)

# iterate through the values of a dictionary
for v in my_dict.values():
    print(v)

# iterate through key, value pairs of a dictionary
for k, v in my_dict.items():
    print(k, ":", v)
