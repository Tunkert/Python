import string

print(string.ascii_letters)  # prints ascii letters

print(string.digits)  # prints the digits 0 - 9
print(string.hexdigits)  # prints all the hexadecimal digits
print(string.octdigits)  # prints all the octal digits 0 - 7
print(string.__all__)  # gives you everything available in the string module

print(string.capwords("my name is Tim"))  # provides title casing


